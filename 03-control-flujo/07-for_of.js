// se da para iterar elementos de un array

let animales= ['perro','gato','elefante','oso','chita','pelicano','burro','caballo'];

for(let animal of animales){
    console.log(animal);
}

console.log('impirmir los elementos del array con while');
let i=0;
while (1< animales.length){
    console.log(animales[i]);
    i++;
}